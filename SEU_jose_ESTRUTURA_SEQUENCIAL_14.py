#faça um programa que peça as 4 notas bimestrais e mostre a media.
numero_1 = float(input("escreva numero_1 :"))
numero_2 = float(input("escreva numero_2 :"))
numero_3 = float(input("escreva numero_3 :"))
numero_4 = float(input("escreva numero_4 :"))
media = (numero_1 + numero_2 + numero_3 + numero_4) / 4
print(f"media {media}")
